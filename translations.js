// Current language
let currentLanguage = 'ru';

// Change language function
function changeLanguage(lang) {
    currentLanguage = lang;
    
    // Update language buttons
    document.getElementById('lang-ru').className = lang === 'ru' 
        ? 'px-3 py-1 rounded-md text-sm font-medium bg-purple-600 text-white transition' 
        : 'px-3 py-1 rounded-md text-sm font-medium text-gray-600 hover:text-purple-600 transition';
    
    document.getElementById('lang-ro').className = lang === 'ro' 
        ? 'px-3 py-1 rounded-md text-sm font-medium bg-purple-600 text-white transition' 
        : 'px-3 py-1 rounded-md text-sm font-medium text-gray-600 hover:text-purple-600 transition';
    
    // Update all translatable elements
    const elements = document.querySelectorAll('[id]');
    elements.forEach(element => {
        const translation = translations[lang][element.id];
        if (translation && element.tagName !== 'INPUT' && element.tagName !== 'TEXTAREA') {
            element.textContent = translation;
        }
    });
    
    // Update placeholder text
    const emailInput = document.querySelector('input[type="email"]');
    if (emailInput) {
        emailInput.placeholder = translations[lang]['payment-email-placeholder'];
    }
    
    // Update plan names and features
    updatePlanContent(lang);
    
    // Save preference
    localStorage.setItem('selectedLanguage', lang);
}

// Update plan content based on language
function updatePlanContent(lang) {
    // Update plan names
    const planNames = document.querySelectorAll('.text-2xl.font-bold.mb-4');
    if (planNames[0]) planNames[0].textContent = translations[lang]['plan-basic'];
    if (planNames[1]) planNames[1].textContent = translations[lang]['plan-standard'];
    if (planNames[2]) planNames[2].textContent = translations[lang]['plan-premium'];
    
    // Update popular badge
    const popularBadge = document.querySelector('.bg-yellow-400');
    if (popularBadge) {
        popularBadge.textContent = translations[lang]['plan-popular'];
    }
    
    // Update plan features
    const basicFeatures = document.querySelectorAll('.hover-scale:first-child ul li span');
    if (basicFeatures[0]) basicFeatures[0].textContent = translations[lang]['basic-feature1'];
    if (basicFeatures[1]) basicFeatures[1].textContent = translations[lang]['basic-feature2'];
    if (basicFeatures[2]) basicFeatures[2].textContent = translations[lang]['basic-feature3'];
    if (basicFeatures[3]) basicFeatures[3].textContent = translations[lang]['basic-feature4'];
    
    const standardFeatures = document.querySelectorAll('.hover-scale:nth-child(2) ul li span');
    if (standardFeatures[0]) standardFeatures[0].textContent = translations[lang]['standard-feature1'];
    if (standardFeatures[1]) standardFeatures[1].textContent = translations[lang]['standard-feature2'];
    if (standardFeatures[2]) standardFeatures[2].textContent = translations[lang]['standard-feature3'];
    if (standardFeatures[3]) standardFeatures[3].textContent = translations[lang]['standard-feature4'];
    if (standardFeatures[4]) standardFeatures[4].textContent = translations[lang]['standard-feature5'];
    
    const premiumFeatures = document.querySelectorAll('.hover-scale:last-child ul li span');
    if (premiumFeatures[0]) premiumFeatures[0].textContent = translations[lang]['premium-feature1'];
    if (premiumFeatures[1]) premiumFeatures[1].textContent = translations[lang]['premium-feature2'];
    if (premiumFeatures[2]) premiumFeatures[2].textContent = translations[lang]['premium-feature3'];
    if (premiumFeatures[3]) premiumFeatures[3].textContent = translations[lang]['premium-feature4'];
    if (premiumFeatures[4]) premiumFeatures[4].textContent = translations[lang]['premium-feature5'];
    if (premiumFeatures[5]) premiumFeatures[5].textContent = translations[lang]['premium-feature6'];
    
    // Update select buttons
    const selectButtons = document.querySelectorAll('button[onclick^="selectPlan"]');
    selectButtons.forEach(button => {
        button.textContent = translations[lang]['plan-select'];
    });
    
    // Update "per video" text
    const perVideoTexts = document.querySelectorAll('.text-sm');
    perVideoTexts.forEach(text => {
        if (text.textContent.includes('/видео') || text.textContent.includes('/video')) {
            text.textContent = translations[lang]['per-video'];
        }
    });
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    const savedLanguage = localStorage.getItem('selectedLanguage') || 'ru';
    changeLanguage(savedLanguage);
});
